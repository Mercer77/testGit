package com.example.bigwork;

public class ChangeGoods {
    private String id;
    private String name;
    private String cheapprice;
    private String changegoods;
    private String introduce;
    private String phone;
    private String zp;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCheapprice() {
        return cheapprice;
    }

    public void setCheapprice(String cheapprice) {
        this.cheapprice = cheapprice;
    }

    public String getChangegoods() {
        return changegoods;
    }

    public void setChangegoods(String changegoods) {
        this.changegoods = changegoods;
    }

    public String getIntroduce() {
        return introduce;
    }

    public void setIntroduce(String introduce) {
        this.introduce = introduce;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getZp() {
        return zp;
    }

    public void setZp(String zp) {
        this.zp = zp;
    }
}